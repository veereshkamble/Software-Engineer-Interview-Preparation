package LinkedList;

public class OddEvenLinkedList {
    public Node oddEvenList(Node head) {
        if (head != null) {

            Node odd = head, even = head.next, evenHead = even;

            while (even != null && even.next != null) {
                odd.next = odd.next.next;
                even.next = even.next.next;
                odd = odd.next;
                even = even.next;
            }
            odd.next = evenHead;
        }
        return head;
    }
}
